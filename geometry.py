def area_of_rectangle(length, breadth):
    rectangle_area = length * breadth
    """
    This function return the area of a rectangle
    """
    return (rectangle_area)

def area_of_circle(radius):
    PI = 3.14
    circle_area = PI * radius * radius
    """
    This function return the area of a rectangle
    """
    return (circle_area)


